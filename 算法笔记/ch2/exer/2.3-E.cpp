#include<cstdio>
int main() {
	double x,s=0;
	scanf("%lf",&x);
	if (x<=100000) {
		s = x*0.1;
	} else if (x<=200000) {
		s = 10000 + (x-100000)*0.075;
	} else if (x<=400000) {
		s = 10000 + 7500 + (x-200000)*0.05;
	} else if (x<=600000) {
		s = 10000 + 7500 + 10000 + (x-400000)*0.03;
	} else if (x<=1000000) {
		s = 10000 + 7500 + 10000 + 6000 + (x-600000)*0.015;
	} else {
		s = 10000 + 7500 + 10000 + 6000 + 6000 + (x-10000)*0.01;
	}
	printf("%.2lf",s);
	return 0;
}
